    public void Table_Pane_Allotments() {
        
        for (int i = 0; i < webTable1_allotment_table.getColumnCount(); i++) {
            DefaultTableColumnModel colModel = (DefaultTableColumnModel) webTable1_allotment_table.getColumnModel();
            TableColumn col = colModel.getColumn(i);
            int width = 500;
            
            TableCellRenderer renderer = col.getHeaderRenderer();
            for (int r = 0; r < webTable1_allotment_table.getRowCount(); r++) {
                renderer = webTable1_allotment_table.getCellRenderer(r, i);
                Component comp = renderer.getTableCellRendererComponent(webTable1_allotment_table, webTable1_allotment_table.getValueAt(r, i),
                        false, false, r, i);
                width = Math.max(width, comp.getPreferredSize().width);
            }
            
            col.setPreferredWidth(width + 400);
            col.setMaxWidth(width);
            col.setWidth(width);
        }
    }
    
