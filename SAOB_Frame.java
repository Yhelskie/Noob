    private void webButton1_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_webButton1_saveActionPerformed
        int unob = Integer.parseInt(webTextField6_allot_rec.getText());
        int todate = Integer.parseInt(webTextField8_todate.getText());
        int total = unob - todate;
        unobligated.setText(String.valueOf(total));
        String SQL_Add_Allot = "Insert into saob_table (year, month, department, agency, fund_code,"
                + " all_obj_class_exp,uacs_code,allot_rec,this_report,to_date,unoblgtd_bal_of_alt)"
                + "values(?,?,?,?,?,?,?,?,?,?,?)";
        
        try {
            pst = (PreparedStatement) conn.prepareStatement(SQL_Add_Allot);
            pst.setString(1, webComboBox2_year.getSelectedItem().toString());
            pst.setString(2, webComboBox1_month.getSelectedItem().toString());
            pst.setString(3, webTextField1_dept.getText());
            pst.setString(4, webComboBox1_agency.getSelectedItem().toString());
            pst.setString(5, webTextField2_fund_code.getText());
            pst.setString(6, webComboBox2_alot_obj_class_exp.getSelectedItem().toString());
            pst.setString(7, webTextField5_uacs_code.getText());
            pst.setString(8, webTextField6_allot_rec.getText());
            pst.setString(9, webTextField7_this_rep.getText());
            pst.setString(10, webTextField8_todate.getText());
            pst.setString(11, unobligated.getText());
            pst.execute();
            Telegraph tel = new Telegraph("Message", "<html>"
                    + "<body style='color:green;font-size:13px'>"
                    + "<b> New Allotment Successfull Saved..."
                    + "</b>"
                    + "</body>"
                    + "</html>", TelegraphType.DOCUMENT_ADD, WindowPosition.TOPRIGHT, 2000);
            TelegraphQueue TQ = new TelegraphQueue();
            TQ.add(tel);
            Table_Pane_Allotments();
            Allotments();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_webButton1_saveActionPerformed

